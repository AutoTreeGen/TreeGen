"""Транслитерация (ROADMAP §5.1.8), первый слайс — ISO 9:1995 для кириллицы.

ISO 9:1995 — однозначное обратимое отображение «один знак ↔ один знак» для
всех современных кириллических алфавитов (русский, украинский,
белорусский, болгарский, сербский) и устаревших букв (ять, фита, ижица),
часто встречающихся в дорев. метриках Восточной Европы XIX–XX вв.

Свойства:

* **Однозначность**: одна кириллическая буква → одна латинская, иногда
  с диакритикой (например, Ж → Ž). Никаких контекстных правил.
* **Обратимость**: по результату всегда можно восстановить исходник.
  Это отличает ISO 9 от «практических» схем (BGN/PCGN, GOST 1971,
  паспортная и т.д.), где Й/Е/Я и т.п. передаются разнобоем.
* **Latin-extended**: использует Юникод-символы с диакритиками
  (Š, Č, Â, Ě, …), не plain ASCII. Для ASCII-варианта планируется
  отдельная схема (BGN/PCGN или ALA-LC) в следующих слайсах.

Не-кириллические символы (Latin, цифры, пунктуация, пробелы) проходят
через функцию без изменений.

YIVO для идиша и ALA-LC для иврита — отдельные слайсы (см. ROADMAP §5.1.8).
"""

from __future__ import annotations

# -----------------------------------------------------------------------------
# Таблица ISO 9:1995
# -----------------------------------------------------------------------------

# Современный русский (33 буквы) + украинские/белорусские/устаревшие.
# Историческая кириллица (ять, фита, ижица) встречается в метриках
# и переписях XIX в. — для генеалогии Восточной Европы это must-have.
_ISO9_BASE: dict[str, str] = {
    # ----- Современный русский -----
    "А": "A",
    "Б": "B",
    "В": "V",
    "Г": "G",
    "Д": "D",
    "Е": "E",
    "Ё": "Ë",
    "Ж": "Ž",
    "З": "Z",
    "И": "I",
    "Й": "J",
    "К": "K",
    "Л": "L",
    "М": "M",
    "Н": "N",
    "О": "O",
    "П": "P",
    "Р": "R",
    "С": "S",
    "Т": "T",
    "У": "U",
    "Ф": "F",
    "Х": "H",
    "Ц": "C",
    "Ч": "Č",
    "Ш": "Š",
    "Щ": "Ŝ",
    "Ъ": "ʺ",  # modifier letter double prime U+02BA
    "Ы": "Y",
    "Ь": "ʹ",  # modifier letter prime U+02B9
    "Э": "È",
    "Ю": "Û",
    "Я": "Â",
    # ----- Украинский / белорусский -----
    "Є": "Ê",
    "І": "Ì",
    "Ї": "Ï",
    "Ґ": "G̀",  # G + combining grave U+0300
    "Ў": "Ŭ",
    # ----- Устаревшая кириллица (XIX в. метрики, дорев. документы) -----
    "Ѣ": "Ě",  # ять
    "Ѳ": "F̀",  # фита (F + combining grave)
    "Ѵ": "Ỳ",  # ижица
}


def _build_full_table(base: dict[str, str]) -> dict[str, str]:
    """Развернуть таблицу: к каждой uppercase-паре добавить lowercase-аналог.

    Lowercase-значение получаем простым ``.lower()`` — Юникод корректно
    переводит латинские диакритики (Ž → ž, Â → â, Ě → ě) и комбинирующие
    знаки (G̀ → g̀).
    """
    full: dict[str, str] = {}
    for src, dst in base.items():
        full[src] = dst
        full[src.lower()] = dst.lower()
    return full


_ISO9_TABLE: dict[str, str] = _build_full_table(_ISO9_BASE)


# str.maketrans поддерживает многосимвольные значения, ключи — int-ordinals
# (один символ-источник на запись). Готовим один раз при импорте.
_ISO9_TRANSLATE: dict[int, str] = str.maketrans(_ISO9_TABLE)


# -----------------------------------------------------------------------------
# Публичный API
# -----------------------------------------------------------------------------


def transliterate_iso9(text: str) -> str:
    """Транслитерировать кириллицу в латиницу по ISO 9:1995.

    * Каждая поддерживаемая буква заменяется ровно на один (или один
      базовый + комбинирующая диакритика) Юникод-символ латиницы.
    * Регистр сохраняется (``Ж`` → ``Ž``, ``ж`` → ``ž``).
    * Не-кириллические символы (Latin, цифры, пунктуация, пробелы,
      переводы строк) возвращаются как есть.

    Результат — обратимый: соответствующая обратная функция (TODO,
    отдельный helper) сможет восстановить исходную строку без потерь.

    Args:
        text: Строка с кириллицей или смесью.

    Returns:
        Транслитерированная строка.

    Examples:
        >>> transliterate_iso9("Иван Иванович Петров")
        'Ivan Ivanovič Petrov'
        >>> transliterate_iso9("Слоним")
        'Slonim'
        >>> transliterate_iso9("123 ABC Иван")
        '123 ABC Ivan'
    """
    return text.translate(_ISO9_TRANSLATE)


def is_cyrillic(text: str) -> bool:
    """Грубая проверка: содержит ли строка хоть один кириллический символ.

    Использует диапазоны Юникода Cyrillic (U+0400..U+04FF) и Cyrillic
    Supplement (U+0500..U+052F). Полезно для решения «надо ли вообще
    запускать транслитерацию».
    """
    return any("Ѐ" <= ch <= "ԯ" for ch in text)


