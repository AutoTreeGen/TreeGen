"""End-to-end тесты CLI `dna-analysis match`.

Использует click.testing.CliRunner — НЕ запускает subprocess, так что
работает в любой CI-среде. Все DNA-файлы — синтетические fixture'ы из
Phase 6.0 (tests/fixtures/synthetic_*.txt) и/или построенные на лету
из tests/matching/_simulators.py.
"""

from __future__ import annotations

import json
import logging
import re
from pathlib import Path

import pytest
from click.testing import CliRunner
from dna_analysis.cli import cli

from tests.matching._simulators import simulate_parent_child_pair


def _write_23andme_file(tmp_path: Path, content: str) -> Path:
    path = tmp_path / "test_23andme.txt"
    path.write_text(content, encoding="utf-8")
    return path


def _write_ancestry_file(tmp_path: Path, content: str) -> Path:
    path = tmp_path / "test_ancestry.txt"
    path.write_text(content, encoding="utf-8")
    return path


def _serialize_test_to_23andme(test) -> str:
    """Сериализует DnaTest назад в 23andMe raw формат для сквозного теста."""
    lines = [
        "# This data file generated by 23andMe at: Mon Jan  1 00:00:00 2024\n",
        "# rsid\tchromosome\tposition\tgenotype\n",
    ]
    for snp in test.snps:
        chr_token = (
            "X"
            if int(snp.chromosome) == 23
            else (
                "Y"
                if int(snp.chromosome) == 24
                else ("MT" if int(snp.chromosome) == 25 else str(int(snp.chromosome)))
            )
        )
        lines.append(f"{snp.rsid}\t{chr_token}\t{snp.position}\t{snp.genotype.value}\n")
    return "".join(lines)


def _serialize_test_to_ancestry(test) -> str:
    """Сериализует DnaTest назад в Ancestry v2 raw формат."""
    lines = [
        "#AncestryDNA Raw DNA Data Download\n",
        "#rsid\tchromosome\tposition\tallele1\tallele2\n",
    ]
    for snp in test.snps:
        gt = snp.genotype.value
        if gt == "NN":
            a1, a2 = "0", "0"
        elif len(gt) == 2:
            a1, a2 = gt[0], gt[1]
        else:
            # hemizygous (single allele) — Ancestry формат не использует;
            # дублируем для simplicity.
            a1 = a2 = gt[0]
        lines.append(f"{snp.rsid}\t{int(snp.chromosome)}\t{snp.position}\t{a1}\t{a2}\n")
    return "".join(lines)


@pytest.fixture
def genetic_map_dir(fixtures_dir: Path) -> Path:
    return fixtures_dir / "genetic_map"


@pytest.fixture
def parent_child_files(tmp_path: Path) -> tuple[Path, Path]:
    parent, child = simulate_parent_child_pair(num_snps=2_500)
    file_a = _write_23andme_file(tmp_path, _serialize_test_to_23andme(parent))
    file_b = _write_ancestry_file(tmp_path, _serialize_test_to_ancestry(child))
    return file_a, file_b


def test_match_outputs_valid_json(
    parent_child_files: tuple[Path, Path], genetic_map_dir: Path
) -> None:
    file_a, file_b = parent_child_files
    runner = CliRunner()
    result = runner.invoke(
        cli,
        ["match", str(file_a), str(file_b), "--genetic-map", str(genetic_map_dir)],
    )
    assert result.exit_code == 0, result.output
    data = json.loads(result.output)

    assert set(data.keys()) == {
        "test_a",
        "test_b",
        "shared_segments",
        "total_shared_cm",
        "longest_segment_cm",
        "relationship_predictions",
        "warnings",
    }


def test_match_summarizes_both_tests(
    parent_child_files: tuple[Path, Path], genetic_map_dir: Path
) -> None:
    file_a, file_b = parent_child_files
    runner = CliRunner()
    result = runner.invoke(
        cli,
        ["match", str(file_a), str(file_b), "--genetic-map", str(genetic_map_dir)],
    )
    data = json.loads(result.output)

    assert data["test_a"]["provider"] == "23andme"
    assert data["test_b"]["provider"] == "ancestry"
    assert data["test_a"]["snp_count"] == 2_500
    assert data["test_b"]["snp_count"] == 2_500
    assert data["test_a"]["reference_build"] == "GRCh37"


def test_match_yields_full_chromosome_segment_for_parent_child(
    parent_child_files: tuple[Path, Path], genetic_map_dir: Path
) -> None:
    file_a, file_b = parent_child_files
    runner = CliRunner()
    result = runner.invoke(
        cli,
        ["match", str(file_a), str(file_b), "--genetic-map", str(genetic_map_dir)],
    )
    data = json.loads(result.output)

    # Parent-child на chr22 → один сегмент через всю хромосому.
    assert len(data["shared_segments"]) >= 1
    seg = data["shared_segments"][0]
    assert seg["chromosome"] == 22
    assert seg["num_snps"] >= 2_000  # ≈ все SNP минус no-call вкрапления
    assert seg["cm_length"] >= 30
    assert data["total_shared_cm"] > 30
    assert data["longest_segment_cm"] > 30


def test_match_includes_relationship_predictions(
    parent_child_files: tuple[Path, Path], genetic_map_dir: Path
) -> None:
    file_a, file_b = parent_child_files
    runner = CliRunner()
    result = runner.invoke(
        cli,
        ["match", str(file_a), str(file_b), "--genetic-map", str(genetic_map_dir)],
    )
    data = json.loads(result.output)

    preds = data["relationship_predictions"]
    assert isinstance(preds, list)
    assert all("label" in p and "probability" in p and "cm_range" in p for p in preds)
    # Source attribution должен быть в каждом prediction.
    assert all("Shared cM Project" in p["source"] for p in preds)


