def test_match_no_warning_for_same_platform(tmp_path: Path, genetic_map_dir: Path) -> None:
    parent, child = simulate_parent_child_pair(num_snps=2_000)
    file_a = tmp_path / "a.txt"
    file_b = tmp_path / "b.txt"
    file_a.write_text(_serialize_test_to_23andme(parent), encoding="utf-8")
    file_b.write_text(_serialize_test_to_23andme(child), encoding="utf-8")

    runner = CliRunner()
    result = runner.invoke(
        cli,
        ["match", str(file_a), str(file_b), "--genetic-map", str(genetic_map_dir)],
    )
    data = json.loads(result.output)
    assert not any("Cross-platform" in w for w in data["warnings"])


def test_match_segments_have_no_raw_genotype_data(
    parent_child_files: tuple[Path, Path], genetic_map_dir: Path
) -> None:
    """Privacy: сегменты в JSON содержат только bp/cM/count, не SNP-кalls."""
    file_a, file_b = parent_child_files
    runner = CliRunner()
    result = runner.invoke(
        cli,
        ["match", str(file_a), str(file_b), "--genetic-map", str(genetic_map_dir)],
    )
    data = json.loads(result.output)

    for seg in data["shared_segments"]:
        assert set(seg.keys()) == {"chromosome", "start_bp", "end_bp", "num_snps", "cm_length"}
        # Не должно быть полей вроде "rsids" или "genotypes".
        assert "rsids" not in seg
        assert "genotypes" not in seg


def test_match_does_not_log_raw_values(
    parent_child_files: tuple[Path, Path],
    genetic_map_dir: Path,
    caplog: pytest.LogCaptureFixture,
) -> None:
    """Privacy guard: логи end-to-end pipeline не содержат raw values."""
    file_a, file_b = parent_child_files
    runner = CliRunner()
    with caplog.at_level(logging.DEBUG):
        runner.invoke(
            cli,
            ["match", str(file_a), str(file_b), "--genetic-map", str(genetic_map_dir)],
        )

    log_text = "\n".join(record.message for record in caplog.records)
    # Не должно быть rsid (rs1, rs2, ...).
    assert not re.search(r"\brs\d+\b", log_text), f"rsid leaked: {log_text!r}"
    # Не должно быть position-like чисел (8+-digit).
    assert not re.search(r"\b\d{8,}\b", log_text), f"position leaked: {log_text!r}"


def test_match_rejects_unknown_format(tmp_path: Path, genetic_map_dir: Path) -> None:
    file_a = tmp_path / "weird.txt"
    file_a.write_text("not a real DNA file\n", encoding="utf-8")
    parent, _ = simulate_parent_child_pair(num_snps=500)
    file_b = tmp_path / "ok.txt"
    file_b.write_text(_serialize_test_to_23andme(parent), encoding="utf-8")

    runner = CliRunner()
    result = runner.invoke(
        cli,
        ["match", str(file_a), str(file_b), "--genetic-map", str(genetic_map_dir)],
    )
    assert result.exit_code != 0
    assert isinstance(result.exception, Exception)


