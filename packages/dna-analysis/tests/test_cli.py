def test_min_cm_and_min_snps_options_propagate(
    parent_child_files: tuple[Path, Path], genetic_map_dir: Path
) -> None:
    """Custom thresholds меняют размер результата."""
    file_a, file_b = parent_child_files
    runner = CliRunner()
    # Очень высокий min_cm должен убрать сегмент целиком.
    result = runner.invoke(
        cli,
        [
            "match",
            str(file_a),
            str(file_b),
            "--genetic-map",
            str(genetic_map_dir),
            "--min-cm",
            "100.0",
        ],
    )
    data = json.loads(result.output)
    assert data["shared_segments"] == []
    assert data["total_shared_cm"] == 0


